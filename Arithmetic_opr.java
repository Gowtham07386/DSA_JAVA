class Arithmetic_opr{
    public static void main(String[] args) {
        int a =8;
        int b =6;

        // Addition
        int result = a + b;
        System.out.println(result);

        //Subraction
        int result1 = a -b;
        System.out.println(result1);

        //multiplication
        int result3 = a*b;
        System.out.println(result3);

        //Division 
        int result4 = a/b;
        System.out.println(result4);

        //Modules
        int result5 = a%b;
        System.out.println(result5);


    }

}