import java.util.Scanner;

public class Contitional_If_else {
    public static void main(String[] args) {
        int x =19;
        if(x<=20 && x>=10){
            System.out.println("x is present b/w 10-20 :"+x);
        }else{
            System.out.println("x is not present b/w 10-20");
        }
        
    }
    
}
